    private void btnGuardarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarUActionPerformed
        if (!camposVaciosUser()) {
            if (!existUser(txtCarnet.getText())) {
                asignarUSer();
                if (Usuario.insert()) {
                    mostrarMensaje(2);
                } else {
                    mostrarMensaje(3);
                }
            } else {
                mostrarMensaje(1);
            }
        } else {
            mostrarMensaje(0);
        }

        limpiarCampos();
        mostrarUser(); 
    }//GEN-LAST:event_btnGuardarUActionPerformed

    private void btnModificarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarUActionPerformed
        if (!camposVaciosUser()) {
            updateUser();
        } else {
            mostrarMensaje(3);
        }
        limpiarCampos();
        mostrarUser();
    }//GEN-LAST:event_btnModificarUActionPerformed

    private void btnEliminarUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUActionPerformed
        if (!campoVacio(idUser.getText())) {
            Usuario.setIdUsuario(Integer.parseInt(idUser.getText()));
            if (existUserID(Usuario.getIdUsuario())) {
                if (Usuario.delete()) {
                    mostrarMensaje(2);
                } else {
                    mostrarMensaje(3);
                }
            } else {
                mostrarMensaje(4);
            }
        } else {
            mostrarMensaje(0);
        }

        limpiarCampos();
        mostrarUser();
    }//GEN-LAST:event_btnEliminarUActionPerformed

    private void tbUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsersMouseClicked
        try {
            int i = tbUsers.getSelectedRow();
            query = "SELECT email, identification, full_name, user_role FROM users WHERE id = ?";
            ResultSet rs = methodsSQL.getExecute(query, tbUsers.getValueAt(i, 0));
            if (rs.next()) {

                Usuario.setTxtEmail(rs.getString(1));
                Usuario.setTxtIdentificacion(rs.getString(2));
                Usuario.setTxtNombreCompleto(rs.getString(3));
                Usuario.setCmbRole(rs.getInt(4));
            }
            idUser.setText(tbUsers.getValueAt(i, 0).toString());
            txtFullName.setText(Usuario.getTxtNombreCompleto());
            txtEmail.setText(Usuario.getTxtEmail());
            txtCarnet.setText(Usuario.getTxtIdentificacion());
            comboRol.setSelectedIndex(Usuario.getCmbRole()- 1);
            
        } catch (SQLException ex) {
            Logger.getLogger(pUsuario.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_tbUsersMouseClicked

    public void updateUser() {
        Usuario.setIdUsuario(Integer.parseInt(idUser.getText()));
        if (existUserID(Usuario.getIdUsuario())) {
            asignarUSer();
            if (Usuario.update()) {
                mostrarMensaje(2);
            } else {
                mostrarMensaje(3);
            }
        } else {
            mostrarMensaje(4);
        }
    }
    
    public void asignarUSer() {
        Usuario.setTxtNombreCompleto(txtFullName.getText());
        Usuario.setTxtEmail(txtEmail.getText());
        Usuario.setTxtIdentificacion(txtCarnet.getText());
        Usuario.setTxtContra(txtPassword.getText());
        Usuario.setCmbRole(methodsSQL.getExecuteInt("SELECT id FROM user_roles WHERE name = ?", comboRol.getSelectedItem().toString()));
    }
    
    public boolean existUser(String txtCarnet) {
        return methodsSQL.exists("SELECT * FROM users WHERE identification = ?", txtCarnet);
    }
    
    public boolean existUserID(Integer idUser) {
        return methodsSQL.exists("SELECT * FROM users WHERE id = ?", idUser);
    }
    
     public boolean camposVaciosUser() {
        if (campoVacio(txtFullName.getText()) || campoVacio(txtEmail.getText()) || campoVacio(txtCarnet.getText()) 
                || campoVacio(txtCarnet.getText()) || comboRol.getSelectedIndex() == -1) {
            return true;
        } else {
            return false;
        }
    }
     
    //Retorna true si el campo est√° vacio
    public boolean campoVacio(String text) {
        text = text.replaceAll(" ", "");
        if (text.isEmpty() || text.length() == 0) {
            return true;
        } else {
            return false;
        }
    }
    
    public void mostrarUser() {
        query = "SELECT u.id, u.email, u.identification, u.full_name, r.name FROM users u, user_roles r WHERE u.user_role = r.id";
        tbUsers.setModel(methodsSQL.getTableModel(query, "ID", "Correo", "Carnet", "Nombre completo", "Rol de Usuario"));
    }
    
    public void cargarComboBoxRole() {
        try {
            ResultSet rs = methodsSQL.getExecute("SELECT name FROM user_roles");
            while (rs.next()) {
                comboRol.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(pEjemplares.class.getName()).log(Level.SEVERE, null, ex);
            

        }
        comboRol.setSelectedIndex(-1);
    }
    
    public void limpiarCampos() {
        txtFullName.setText("");
        txtEmail.setText("");
        txtCarnet.setText("");
        txtPassword.setText("");
        comboRol.setSelectedIndex(-1);
    }
    
    //Mensajes
    public void mostrarMensaje(int value) {
        switch (value) {
            case 0:
                JOptionPane.showMessageDialog(this, "Campos vacios", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Ese id ya exciste", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Exito al ejecutar", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                break;
            case 3:
                JOptionPane.showMessageDialog(this, "Error al ejecutar", "Mensaje", JOptionPane.ERROR_MESSAGE);
                break;
            case 4:
                JOptionPane.showMessageDialog(this, "Ese id no existe", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
        }
    }

