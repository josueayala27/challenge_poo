    private void tbSeccionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSeccionMouseClicked
        try {
            int i = tbSeccion.getSelectedRow();
            query = "SELECT section, location_id from location_sections WHERE id = ?";
            ResultSet rs = methodsSQL.getExecute(query, tbSeccion.getValueAt(i, 0));
            if (rs.next()) {

                Seccion.setTxtSeccion(rs.getString(1));
                Seccion.setCmbLocacion(rs.getInt(2));
            }
            idSec.setText(tbSeccion.getValueAt(i, 0).toString());
            txtSeccion.setText(Seccion.getTxtSeccion());
            cmbLocalizacion.setSelectedIndex(Seccion.getCmbLocacion()- 1);
            
        } catch (SQLException ex) {
            Logger.getLogger(pLocalidad.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_tbSeccionMouseClicked

