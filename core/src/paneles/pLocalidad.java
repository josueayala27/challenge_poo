    public void updateLocalidad() {
        Localidad.setIdLocalidad(Integer.parseInt(idLoc.getText()));
        if (existLocalidadID(Localidad.getIdLocalidad())) {
            asignarLocalidad();
            if (Localidad.update()) {
                mostrarMensaje(2);
            } else {
                mostrarMensaje(3);
            }
        } else {
            mostrarMensaje(4);
        }
    }
    
    //Modificar valores
    public void updateSeccion() {
        Seccion.setIdSeccion(Integer.parseInt(idSec.getText()));
        if (existSeccionID(Seccion.getIdSeccion())) {
            asignarSeccion();
            if (Seccion.update()) {
                mostrarMensaje(2);
            } else {
                mostrarMensaje(3);
            }
        } else {
            mostrarMensaje(4);
        }
    }
    
    public void asignarLocalidad() {
        Localidad.setTxtLocalidad(txtLocation.getText());
    }
    
    public void asignarSeccion() {
        Seccion.setTxtSeccion(txtSeccion.getText());
        Seccion.setCmbLocacion(methodsSQL.getExecuteInt("SELECT id FROM locations WHERE location = ?", cmbLocalizacion.getSelectedItem().toString()));
    }
    
    public boolean existSeccion(String txtSeccion) {
        return methodsSQL.exists("SELECT * FROM location_sections WHERE section = ?", txtSeccion);
    }
    
    public boolean existSeccionID(Integer idSec) {
        return methodsSQL.exists("SELECT * FROM location_sections WHERE id = ?", idSec);
    }
    
    public boolean existLocalidad(String txtLocation) {
        return methodsSQL.exists("SELECT * FROM locations WHERE location = ?", txtLocation);
    }
    
    public boolean existLocalidadID(Integer idLoc) {
        return methodsSQL.exists("SELECT * FROM locations WHERE id = ?", idLoc);
    }
    
    public boolean camposVaciosLocalidad() {
        if (campoVacio(txtLocation.getText())) {
            return true;
        } else {
            return false;
        }
    }
    
    public boolean camposVaciosSeccion() {
        if (campoVacio(txtSeccion.getText()) || cmbLocalizacion.getSelectedIndex() == -1) {
            return true;
        } else {
            return false;
        }
    }
    
    //Retorna true si el campo est√° vacio
    public boolean campoVacio(String text) {
        text = text.replaceAll(" ", "");
        if (text.isEmpty() || text.length() == 0) {
            return true;
        } else {
            return false;
        }
    }
    
    public void mostrarSeccion() {
        query = "select s.id, s.section, l.location from location_sections s, locations l WHERE s.location_id = l.id";
        tbSeccion.setModel(methodsSQL.getTableModel(query, "ID", "Seccion", "Localidad"));
    }
    
    public void mostrarLocalidad() {
        query = "select l.id, l.location from locations l";
        tbLocalizacion.setModel(methodsSQL.getTableModel(query, "ID", "Pasillo"));
    }
    
    public void cargarComboBoxLocacion() {
        try {
            ResultSet rs = methodsSQL.getExecute("SELECT location FROM locations");
            while (rs.next()) {
                cmbLocalizacion.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(pEjemplares.class.getName()).log(Level.SEVERE, null, ex);
            

        }
        cmbLocalizacion.setSelectedIndex(-1);
    }
    
    public void limpiarCampos() {
        idSec.setText("");
        txtSeccion.setText("");
        cmbLocalizacion.setSelectedIndex(-1);
        
        idLoc.setText("");
        txtLocation.setText("");

    }
    
     //Mensajes
    public void mostrarMensaje(int value) {
        switch (value) {
            case 0:
                JOptionPane.showMessageDialog(this, "Campos vacios", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 1:
                JOptionPane.showMessageDialog(this, "Ese id ya exciste", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
            case 2:
                JOptionPane.showMessageDialog(this, "Exito al ejecutar", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                break;
            case 3:
                JOptionPane.showMessageDialog(this, "Error al ejecutar", "Mensaje", JOptionPane.ERROR_MESSAGE);
                break;
            case 4:
                JOptionPane.showMessageDialog(this, "Ese id no existe", "Advertencia", JOptionPane.WARNING_MESSAGE);
                break;
        }
    }

